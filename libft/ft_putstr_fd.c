#include "libft.h"

int	ft_putstr_fd(char *s, int fd)
{
	int	i;
	int	c;
	
	c = 0;
	i = 0;
	if (!s)
		return (0);
	while (s[i])
		c += ft_putchar_fd(s[i++], fd);
	return (c);
}
